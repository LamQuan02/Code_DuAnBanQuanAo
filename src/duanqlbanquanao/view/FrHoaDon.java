        modelHD = (DefaultTableModel) tblTable.getModel();
        modelHD.setRowCount(0);
        List<HoaDon> dsHoaDons = hoaDonService.search(txt);
        for (HoaDon hd : dsHoaDons) {
            modelHD.addRow(new Object[]{
                hd.getMa(), hd.getNguoiTao(), hd.getNgayTao(), hd.getTrangThai()
            });
        }
