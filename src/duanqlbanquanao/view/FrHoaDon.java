    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblTable.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Moi ban chon");
            return;
        }
        int choice = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Xoá Không?", "delete", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            int ma = (int) model.getValueAt(index, 0);
            xoaThongTin(ma);
            model.removeRow(index);
            JOptionPane.showMessageDialog(this, "Xoa thanh cong");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTableMouseClicked
        int index = tblTable.getSelectedRow();
        String nguoiTao = model.getValueAt(index, 1).toString();
        String ngayTao = model.getValueAt(index, 2).toString();
        String trangThai = model.getValueAt(index, 3).toString();
        String ma = model.getValueAt(index, 0).toString();
        txtNguoiTao.setText(nguoiTao);
        txtNgayTao.setText(ngayTao);
        txtMaHD.setText(ma);
        if (trangThai.equalsIgnoreCase("Đã Thanh Toán")) {
            rdDaTT.setSelected(true);
        }
        if (trangThai.equalsIgnoreCase("Chưa Thanh Toán")) {
            rdCTT.setSelected(true);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_tblTableMouseClicked

