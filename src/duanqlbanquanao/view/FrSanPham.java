    private void layDuLieu() {
        List<SanPham> list = this.sanPhamService.layDanhSach();
        for (SanPham sp : list) {
            int ma = sp.getMa();
            String ten = sp.getTen();
            int soLuong = sp.getSoLuong();
            float gia = sp.getGia();
            model.addRow(new Object[]{ma, ten, soLuong, gia});
        }
    }

