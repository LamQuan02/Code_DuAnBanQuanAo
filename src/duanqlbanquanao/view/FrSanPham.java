    private SanPham validateSanPham() {
        String ten = txtTen.getText();
        if (ten.isBlank()) {
            JOptionPane.showMessageDialog(this, "Nhap ten");
            return null;
        }
        String soLuong = txtSoLuong.getText();
        if (soLuong.isBlank()) {
            JOptionPane.showMessageDialog(this, "Nhap So Luong");
            return null;
        }
        int soLuongInt;
        try {
            soLuongInt = Integer.parseInt(soLuong);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "So Luong Khong Hop Le");
            return null;
        }
        String gia = txtGia.getText();
        if (gia.isBlank()) {
            JOptionPane.showMessageDialog(this, "Nhap Gia");
            return null;
        }

        float giaFloat;
        try {
            giaFloat = Float.parseFloat(gia);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Gia khong hop le");
            return null;
        }

        SanPham sp = new SanPham();
        sp.setTen(ten);
        sp.setSoLuong(soLuongInt);
        sp.setGia(giaFloat);
        return sp;
    }
